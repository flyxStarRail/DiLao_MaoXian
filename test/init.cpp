void next_map(NewAreaList*& ar, NewAreaList* ar_l[], int& index, HeroMoveAttribute* hero, int*& x)
{
	ar = ar_l[index];
	hero->link(ar);
	hero->attrclear();
	hero->position_clear();
	*x = 0;
	x = &ar->get_screen_x();

}

void restart(NewAreaList*& ar, NewAreaList* ar_l[], int& index, HeroMoveAttribute* hero, int*& x)
{
	index = 0;
	ar = ar_l[index];
	hero->link(ar);
	hero->attrclear();
	hero->position_clear();
	*x = 0;
	x = &ar->get_screen_x();
	for (int i = 0; i < MAP_NUM; i++)
	{
		ar_l[i]->restart();
	}
	SellItem::restart();
}